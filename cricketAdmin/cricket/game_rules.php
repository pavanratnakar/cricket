<h1>GAME RULES</h1>
<h3>Common Cricket rules</h3>
<ul>
	<li>All extras except Legbyes are counted as runs.</li>
	<li>Runs on overthrows will be counted.</li>
	<li>Free hit is there only for overstepping. (<i>No ball</i>)</li>
	<li>Chucking and throw bowling is not allowed excluding Lady Mind players. (<i>Umpire has to take the decision</i>)</li>
	<li>Only one bouncer is allowed but it should not be above head and beamer.</li>
</ul> 
<h3>Special P&amp;G Digital Business Cricket Rules</h3>
<ul>
	<li>Total 12 overs per innings as 10 Regular Overs and 2 super overs.</li>
	<li>Lady Mind should bowl minimum One over excluding the super overs.</li>
	<li>Maximum overs per bowler is  2 excluding super over.</li>
	<li>No stumping and runouts on legbyes.</li>
	<li>No field restriction.</li>
	<li>Fielder should not be straight to pitch.</li>
	<li>Keeper should not bowl.</li>
	<li>90 minutes per inning allowed, in case of any delay from the bowling team one normal over will be reduced for them while batting.</li>
	<li>10 minutes is provided as innings break.</li>
	<li>Umpire decision will be final. <b>No arguments on Umpire decisions</b>.</li>
</ul>
<h3>Super over Rules</h3>
<ul>
	<li>For Wides, team can decide to have extra run (<i>2 runs</i>) or extra ball with one run.</li>
	<li>4's will be counted as 6 runs and 6's will be counted as 8 runs.</li>
	<li>5 runs will be reduced from the Batting team if the player gets out in super overs.</li>
</ul>
