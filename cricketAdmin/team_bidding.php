<h1>TEAM BIDDING RULES</h1>
<h3>Rules</h3>
<ul>
	<li>The per team cap is Rs. 2.5 Cr.</li>
	<li>Every team will have 2 Owners and 1 Treasurer.</li>
	<li>Treasurer cannot be a player.</li>
	<li>Each team should have minimum 13 + 2 (girls) = 15 players.</li>
	<li>It is mandatory for the female player to play in Final XI.</li>
	<li>If any of the team-owners are playing then his base price will be deducted from Team BID CAP.</li>
	<li>It is not mandatory for Team Owners/Co-Owners to play.</li>
	<li>Every team name should consist of "P&amp;G Product Name + Your Chosen Name". Eg. Vicks Vipers.</li>
</ul>
